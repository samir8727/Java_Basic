//If Else conditional Statement.
class Program4{
	public static void main(String[] args) {
		int x =10;
		int y;//not initialized
		if (x==10){
			y=20;
			System.out.println(x);
			System.out.println(y);
		}
		else{
			y=30;
			System.out.println(x);
			System.out.println(y);
		}
		System.out.println(y);//here the value of y is depend upon the condition .
	}
}